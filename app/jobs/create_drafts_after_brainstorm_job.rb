# Job to generate all drafts after brainstorm is completed
# This job is used when creating a new article from existing transcript
# and immediately generating both brainstorm and drafts
class CreateDraftsAfterBrainstormJob < ApplicationJob
  queue_as :default

  def perform(stream_name:, article_id:, writing_style: 'original')
    article = Article.find_by(id: article_id)
    unless article
      Rails.logger.error "Article not found: #{article_id}"
      return
    end

    Rails.logger.info "Starting draft generation for article #{article_id} with writing_style: #{writing_style}"

    # List of all available providers
    providers = ['grok', 'qwen', 'deepseek', 'gemini', 'zhipu']

    # Generate drafts for all providers that have completed brainstorm
    providers.each do |provider|
      # Check if brainstorm exists for this provider
      brainstorm_content = article.send("brainstorm_#{provider}")

      # Skip if no brainstorm content yet
      if brainstorm_content.blank?
        Rails.logger.warn "Skipping #{provider} draft - brainstorm not ready yet"
        next
      end

      # Set draft status to pending
      article.set_draft_status(provider, 'pending')

      # Get model display name
      model_display_name = case provider
      when 'grok' then 'Grok'
      when 'qwen' then 'Qwen'
      when 'deepseek' then 'DeepSeek'
      when 'gemini' then 'Gemini'
      when 'zhipu' then 'æ™ºè°±'
      else provider.capitalize
      end

      # Build draft prompt with writing style
      draft_prompt = build_draft_prompt(article.transcript, brainstorm_content, model_display_name, writing_style)

      llm_config = get_llm_config(provider)
      llm_config_with_timeout = llm_config.merge(timeout: 240, max_tokens: 8000)

      LlmStreamJob.perform_later(
        stream_name: "#{stream_name}_draft_#{provider}",
        prompt: draft_prompt,
        llm_config: llm_config_with_timeout,
        article_id: article.id,
        provider: "draft_#{provider}",
        streaming: false
      )

      Rails.logger.info "Triggered draft generation for #{provider}"
    end

    # Broadcast that draft generation started
    ActionCable.server.broadcast(
      stream_name,
      {
        type: 'all-drafts-started',
        article_id: article.id
      }
    )

    Rails.logger.info "Completed triggering draft generation for article #{article_id}"
  end

